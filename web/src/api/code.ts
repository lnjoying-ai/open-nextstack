/**
 * 显示错误码对应的消息
 */
const showCodeMessage = (data: any): string => {
  let textData = '';
  if (data.data.code && codeMessageMap[JSON.stringify(data.data.code)]) {
    textData = codeMessageMap[JSON.stringify(data.data.code)];
  } else if (data.data.message) {
    textData = data.data.message;
  } else if (data.status) {
    textData = codeMessageMap[JSON.stringify(data.status)];
  }
  return textData || i18n.global.t('page.networkError');
};

export default showCodeMessage;
